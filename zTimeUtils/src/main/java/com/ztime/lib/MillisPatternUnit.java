package com.ztime.lib;

public class MillisPatternUnit {

    /**
     * 一天的毫秒数
     */
    public static final int DAY_MILLIS = 1000 * 24 * 60 * 60;

    /**
     * 一小时的毫秒数
     */
    public static final int HOUR_MILLIS = 1000 * 60 * 60;

    /**
     * 一分钟的毫秒数
     */
    public static final int MIN_MILLIS = 1000 * 60;

    /**
     * 一秒钟的毫秒数
     */
    public static final int SEC_MILLIS = 1 * 1000;

}
