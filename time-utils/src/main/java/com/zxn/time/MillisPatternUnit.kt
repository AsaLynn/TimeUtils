package com.zxn.time;

/**
 * TimePatternUnit
 * Updated by zxn on 2020/4/28.
 */
public interface MillisPatternUnit {

    /**
     * 一天的毫秒数
     */
    int DAY_MILLIS = 1000 * 24 * 60 * 60;

    /**
     * 一小时的毫秒数
     */
    int HOUR_MILLIS = 1000 * 60 * 60;

    /**
     * 一分钟的毫秒数
     */
    int MIN_MILLIS = 1000 * 60;

    /**
     * 一秒钟的毫秒数
     */
    int SEC_MILLIS = 1000;

}
